namespace ScanApp.Components.Table.Enums
{
    /// <summary>
    /// Visibility of table option.
    /// </summary>
    public enum Show
    {
        /// <summary>
        /// Table decides if given option is visible.
        /// </summary>
        Auto = 0,

        /// <summary>
        /// Given option is always visible.
        /// </summary>
        Yes,

        /// <summary>
        /// Given option is never visible.
        /// </summary>
        No
    }
}